package pe.edu.uni.demo03.dto;

public class MateDto {
	
	// Datos de entrada
	private int num1;
	private int num2;
	// Datos de salida
	private int suma;
	private int resta;

	public int getNum1() {
		return num1;
	}

	public void setNum1(int num1) {
		this.num1 = num1;
	}

	public int getNum2() {
		return num2;
	}

	public void setNum2(int num2) {
		this.num2 = num2;
	}

	public int getSuma() {
		return suma;
	}

	public void setSuma(int suma) {
		this.suma = suma;
	}

	public int getResta() {
		return resta;
	}

	public void setResta(int resta) {
		this.resta = resta;
	}
	
	
	
	
	
}
